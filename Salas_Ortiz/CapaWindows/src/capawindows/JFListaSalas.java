    @Override
    public void Actualiza(GestorSala gesObsSalas) {
         
       ArrayList<Sala> listaSalas = new ArrayList<>();
       listaSalas = gesObsSalas.GetSalas();
        
        DefaultTableModel modelo = (DefaultTableModel)this.tabla.getModel();
        int a = tabla.getRowCount()-1;
        for (int i = a; i >= 0; i--) {
            modelo.removeRow(modelo.getRowCount()-1);
        }
        for(int i=0; i<gesObsSalas.size(); i++){
             modelo.addRow(new Object[]{listaSalas.get(i).getNombreSala(),listaSalas.get(i).getNumSala()});
        }
    }
